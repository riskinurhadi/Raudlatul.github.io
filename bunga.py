import turtle

# Create a turtle object and set the pen color to purple
t = turtle.Turtle()
t.pencolor("purple")

# Set the turtle's speed to the maximum value
t.speed(5)

# Move the turtle to the center of the screen
t.penup()
t.goto(0, 0)
t.pendown()

# Draw the flower pattern
for i in range(36):
    t.forward(100)
    t.right(170)



